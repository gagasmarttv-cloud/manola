    //filtruje udaje v tabulke zakaznikov
    private void filtrujZakaznika(){
        
        TableRowSorter filter_zakaznik = new TableRowSorter(tbl_zakaznik.getModel());
        
        tbl_zakaznik.setRowSorter(filter_zakaznik);
        
        RowFilter  rf = null;
        
        try {
             rf = RowFilter.regexFilter(jtf_filter_zakaznik.getText());
             }
        catch (java.util.regex.PatternSyntaxException e) {
            return;
             }
        filter_zakaznik.setRowFilter(rf);
    }
    
    
