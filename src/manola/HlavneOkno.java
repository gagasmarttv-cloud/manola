    /**
     *nacita a zobrazi informacie do tabuliek
     *
     **/
    private void nacitajUdajeZakaznik(){
       DefaultTableModel mdl = new DefaultTableModel();
       
       tbl_zakaznik.setModel(mdl);
       
       while(tbl_zakaznik.getRowCount() > 0){
            mdl.removeRow(0);
       }
       
       mdl.addColumn("Rodne cislo");
       mdl.addColumn("Cislo OP");
       mdl.addColumn("Meno");
       mdl.addColumn("Adresa");
      
       
       Databaza db = new Databaza();
       
       Object[] zakaznici = db.vratPouzivatelov();
             
       for (int i = 0; i < zakaznici.length; i++) {
           Pouzivatel novy = (Pouzivatel)zakaznici[i];
           mdl.addRow(new Object[] {novy.cislo_OP.toString(), novy.doplnk_info.toString(), novy.meno.toString()+" "+novy.priezvisko.toString(), novy.adresa.toString()});
       }
    }   
    
    /**
     *Nacita udaje o zmluvach
     **/
    private void nacitajUdajeZmluva(){
       DefaultTableModel mdl = new DefaultTableModel();
       
       tbl_zmluvy.setModel(mdl);
       
       while(tbl_zmluvy.getRowCount() > 0){
            mdl.removeRow(0);
       }
       
       mdl.addColumn("ID");
       mdl.addColumn("Zakaznik");
       mdl.addColumn("Predmet");
       mdl.addColumn("Suma");
       mdl.addColumn("Stav");
       mdl.addColumn("Poznamka");
       
       Databaza db = new Databaza();
       
       Object[] zmluvy = db.vratZmluvy();
           
       for (int i = 0; i < zmluvy.length; i++) {
           Zmluva nova = (Zmluva)zmluvy[i];
           if(nova.datum.getYear() == 2008){
               mdl.addRow(new Object[] {nova.ID.toString(), nova.zakaznik_OP.toString(), nova.predmet.toString(), nova.hodnota+" SK ", nova.stav.toString(), nova.poznamka.toString()});              
           }
           else{
               mdl.addRow(new Object[] {nova.ID.toString(), nova.zakaznik_OP.toString(), nova.predmet.toString(), nova.hodnota+" € ", nova.stav.toString(), nova.poznamka.toString()});
           }
           
       }
    }
    
